numero = prompt("Digite um número");
// Vamos calcular o número digitado em busca do resto da divição
// se o resto for igual a 0(zero), então o número digitado é Par
// caso contrário, será Impar
if(numero % 2 == 0){
    alert ("O número digitado é Par");
}
else{
    alert ("O número digitado é Impar");
}